#include "term3d.h"

void	print_p_3d(t_p_3d *p_3ds, const int p_num)
{
	int		i;

	i = 0;
	while (i < p_num)
	{
		printf("x:%lf, y:%lf, z:%lf\n", p_3ds[i].x, p_3ds[i].y, p_3ds[i].z);
		i++;
	}
}
